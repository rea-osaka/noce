#' read residence data
#'
#' This function reads excel files for data of number of
#' construction for residence.
#'
#' @param file_path_v vector of excel files path
#'
#' @return tibble
#' @importFrom magrittr %>%
#' @importFrom rlang :=
#' @export
noce_residence_read_files <- function(file_path_v){

  ans_list <- vector("list", length(file_path_v))

  for(i in seq_along(file_path_v)){
    ans_list[[i]] <- .noce_residence_read_a_file(file_path_v[i])
  }

  ans <- dplyr::bind_rows(ans_list)

  return(ans)
}



# １ファイルの読み込みルーチン
.noce_residence_read_a_file <- function(file_path){

  # 2シート分
  row_obj1 <- readxl::read_excel(file_path, sheet = 1, col_names = .t15_names)
  row_obj2 <- readxl::read_excel(file_path, sheet = 2, col_names = .t15_names)

  # 日付取得
  .target_data <- .find_date_string(row_obj1) %>% .into_dateobj()

  # 余計なものを省いて
  row_obj <- row_obj1 %>% .clean_rowdata() %>%
    dplyr::bind_rows(row_obj2 %>% .clean_rowdata()) %>%
    .add_area_item(.localDB_area_t15) %>%
    .add_date_item(.target_data)

  # longer pivotの処理
  ans <-
    list(row_obj) %>%
    .make_dividedlist(1,"cat02_code") %>%
    .make_dividedlist(2,"cat01_code") %>%
    .make_dividedlist(3,"tab_code")

  df_ans <- NULL
  for(i in seq_along(ans)){
    df_ans  <- dplyr::bind_rows(df_ans, ans[[i]] %>% .rename_value())
  }

  # コード対応表の追加

  ans <- df_ans %>%
    dplyr::left_join(.localDB_tab_t15, by = "tab_code") %>%
    dplyr::left_join(.localDB_cat01_t15, by = "cat01_code") %>%
    dplyr::left_join(.localDB_cat02_t15, by = "cat02_code")

  # 整頓
  ans <- ans %>%
    dplyr::select(-.data$area) %>%
    dplyr::select(.data$tab_code, .data$`表章項目`,
                  .data$cat01_code, .data$`利用関係`,
                  .data$cat02_code, .data$`工事`,
                  .data$area_code, .data$`地域`,
                  .data$time_code, .data$`時間軸(月次)`,
                  .data$unit,
                  .data$value) %>%
    dplyr::mutate(value = as.numeric(.data$value))

  return(ans)
}

